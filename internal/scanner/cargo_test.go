func TestCargoParser_CargoToml_DepsAndDevDeps(t *testing.T) {
	dir := t.TempDir()
	content := `[package]
name = "myproject"
version = "0.1.0"

[dependencies]
serde = "1.0"
tokio = { version = "1.34", features = ["full"] }

[dev-dependencies]
criterion = "0.5"
`
	writeTempFile(t, dir, "Cargo.toml", content)

	p := &CargoParser{}
	deps, err := p.Parse(filepath.Join(dir, "Cargo.toml"))
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if len(deps) != 3 {
		t.Fatalf("expected 3 deps, got %d", len(deps))
	}

	depMap := map[string]models.Dependency{}
	for _, d := range deps {
		depMap[d.Name] = d
	}

	// Simple string version
	if depMap["serde"].Version != "1.0" {
		t.Errorf("expected 1.0, got %s", depMap["serde"].Version)
	}

	// Inline table version extraction
	if depMap["tokio"].Version != "1.34" {
		t.Errorf("expected 1.34, got %s", depMap["tokio"].Version)
	}
	if depMap["tokio"].IsDev {
		t.Error("tokio should not be dev")
	}

	// Dev dependency
	if !depMap["criterion"].IsDev {
		t.Error("criterion should be dev")
	}
}

