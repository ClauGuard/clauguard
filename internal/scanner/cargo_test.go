func TestCargoParser_CargoToml_SectionBoundary(t *testing.T) {
	dir := t.TempDir()
	content := `[dependencies]
serde = "1.0"

[build-dependencies]
cc = "1.0"

[dev-dependencies]
test-log = "0.2"
`
	writeTempFile(t, dir, "Cargo.toml", content)

	p := &CargoParser{}
	deps, err := p.Parse(filepath.Join(dir, "Cargo.toml"))
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	// [build-dependencies] is a new [section], so parsing stops for deps
	// then [dev-dependencies] starts a new recognized section
	if len(deps) != 2 {
		t.Fatalf("expected 2 deps (serde + test-log), got %d", len(deps))
	}

	depMap := map[string]models.Dependency{}
	for _, d := range deps {
		depMap[d.Name] = d
	}

	if _, ok := depMap["cc"]; ok {
		t.Error("build-dependencies should not be parsed as deps or dev-deps")
	}
}

func TestCargoParser_CargoLock_MultiplePackages(t *testing.T) {
	dir := t.TempDir()
	content := `# This file is automatically @generated by Cargo.
[[package]]
name = "serde"
version = "1.0.193"

[[package]]
name = "tokio"
version = "1.34.0"

[[package]]
name = "libc"
version = "0.2.150"
`
	writeTempFile(t, dir, "Cargo.lock", content)

	p := &CargoParser{}
	deps, err := p.Parse(filepath.Join(dir, "Cargo.lock"))
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if len(deps) != 3 {
		t.Fatalf("expected 3 deps, got %d", len(deps))
	}

	depMap := map[string]models.Dependency{}
	for _, d := range deps {
		depMap[d.Name] = d
	}

	if depMap["serde"].Version != "1.0.193" {
		t.Errorf("expected 1.0.193, got %s", depMap["serde"].Version)
	}
	if depMap["tokio"].Version != "1.34.0" {
		t.Errorf("expected 1.34.0, got %s", depMap["tokio"].Version)
	}
	// Last package should not be forgotten
	if depMap["libc"].Version != "0.2.150" {
		t.Errorf("expected 0.2.150, got %s", depMap["libc"].Version)
	}
}

func TestCargoParser_CargoLock_LastPackageNotForgotten(t *testing.T) {
	dir := t.TempDir()
	// Only one package, no trailing [[package]] to flush it
	content := `[[package]]
name = "only-one"
version = "0.1.0"
`
	writeTempFile(t, dir, "Cargo.lock", content)

	p := &CargoParser{}
	deps, err := p.Parse(filepath.Join(dir, "Cargo.lock"))
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if len(deps) != 1 {
		t.Fatalf("expected 1 dep, got %d", len(deps))
	}
	if deps[0].Name != "only-one" {
		t.Errorf("expected only-one, got %s", deps[0].Name)
	}
}

